use super::Fixed32;
use crate::math::types::FRACTIONAL;

impl Fixed32 {
    #[inline(always)]
    pub fn sqrt(&self) -> Self {
        let mut scale = 0usize;
        let mut x = if self.inner < 0 {
            panic!("GRRR NEGATIVE NUMBER");
        } else {
            self.inner
        };

        while x >= (SQRT_LUT.len()) as i32 {
            x = x >> 2;
            scale += 1;
        }

        SQRT_LUT[(x) as usize] << scale >> const { FRACTIONAL / 2 }
    }

    // python3:
    // python -c 'for i in range(4096): print(f"        Fixed32::from_f32({__import__("math").sqrt(i+1)}),")' > SQRT_LUT.txt
}

